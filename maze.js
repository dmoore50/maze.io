export function draw(gameBoard) {
    wallPlacement.forEach (segment => {
        const wallElement = document.createElement('div')
        wallElement.style.gridRowStart = segment.y
        wallElement.style.gridColumnStart = segment.x
        wallElement.classList.add('wall')
        gameBoard.appendChild(wallElement)
    })
}

export function drawEndPoint(gameBoard) {
    endPoint.forEach (segment => {
        const endPointElement = document.createElement('div')
        endPointElement.style.gridRowStart = segment.y
        endPointElement.style.gridColumnStart = segment.x
        endPointElement.classList.add('endPoint')
        gameBoard.appendChild(endPointElement)
    })
}

export function outsideGrid(position) {
    return ( 
        position.x < 1 || position.x > gridSize ||
        position.y < 1 || position.y > gridSize
    )
}
